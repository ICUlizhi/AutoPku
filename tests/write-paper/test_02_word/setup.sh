#!/bin/bash
set -e

COURSE_DIR="test00/财务报表分析"
mkdir -p "${COURSE_DIR}/通知"
mkdir -p "${COURSE_DIR}/资料"
mkdir -p "${COURSE_DIR}/论文"

# 模拟论文要求文件
cat > "${COURSE_DIR}/通知/期末论文要求.txt" << 'REQEOF'
财务报表分析课程期末论文要求

题目：自选一家上市公司进行财务分析
字数：5000字左右
格式：Word (.docx)
提交：教学网提交
截止日期：2026-06-25

要求：
1. 包含摘要、关键词
2. 至少3张图表
3. 引用参考文献不少于5篇
REQEOF

# 模拟大纲
cat > "${COURSE_DIR}/论文/outline.md" << 'OUTEOF'
# 特变电工财务分析报告

## 一、引言（约 500 字）
- 研究背景与意义
- 公司简介

## 二、财务报表分析（约 2000 字）
- 资产负债表分析
- 利润表分析
- 现金流量表分析

## 三、财务比率分析（约 1500 字）
- 偿债能力分析
- 营运能力分析
- 盈利能力分析

## 四、结论与建议（约 1000 字）
- 总结
- 投资建议

## 参考文献
1. 张三. 上市公司财务分析研究[J]. 会计研究, 2023.
2. 李四. 新能源行业财务分析[M]. 北京: 经济科学出版社, 2024.
OUTEOF

# 模拟已生成的 paper.docx（实际由 python-docx 生成，这里用脚本在 validate 时生成）
# 不预生成 docx，让 validate 脚本检查缺失时正确报告

echo "setup complete"
